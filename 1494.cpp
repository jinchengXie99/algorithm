#include <iostream>
#include <iomanip>
using namespace std;
int main()
{
    double p,w;
    cin>>p;
    if(p<=100000)
    w=p*0.1;
    else if(p>100000 && p<=200000)
    w=100000*0.1+(p-100000)*0.075;
    else if(p>200000 && p<=400000)
    w=100000*0.1+100000*0.075+(p-200000)*0.05;
    else if(p>400000 && p<=600000)
    w=100000*0.1+100000*0.075+200000*0.05+(p-400000)*0.03;
    else if(p>600000 && p<=1000000)
    w=100000*0.1+100000*0.075+200000*0.05+200000*0.03+(p-600000)*0.015;
    else
    w=100000*0.1+100000*0.075+200000*0.05+200000*0.03+400000*0.015+(p-1000000)*0.01;
    cout<<setiosflags(ios::fixed)<<setprecision(2);
    cout<<w<<endl;
    return 0;
}
